using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using Web_UshtrimePerProvim_LoansApp.Models;

namespace Web_UshtrimePerProvim_LoansApp.Controllers
{
    public class PersonController : Controller
    {
        private readonly ApplicationDbContext _context;

        public PersonController(ApplicationDbContext context)
        {
            _context = context;
        }

        // GET: Person
        public async Task<IActionResult> Index(string name, string lastname)
        {
            if(string.IsNullOrEmpty(name) || string.IsNullOrEmpty(lastname))
            {
                return View(await _context.People.ToListAsync());

            }
            var list = await _context.People.Include(x => x.Applications).Where(x => x.Name.ToLower().Contains(name.ToLower())&& x.Name.ToLower().Contains(name.ToLower())
            && x.Applications.Any()).ToListAsync();

            return View(list);

            //return _context.People != null ?
            //            View(await _context.People.ToListAsync()) :
            //            Problem("Entity set 'ApplicationDbContext.People'  is null.");

        }

        //[HttpGet]
        //public async Task<IActionResult> GetWithLoans()
